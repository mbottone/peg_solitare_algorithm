import Foundation

let board = Board(data: [
    [.Empty, .Peg, .Peg, .Peg, .Peg],
    [.Peg, .Peg, .Peg, .Peg],
    [.Peg, .Peg, .Peg],
    [.Peg, .Peg],
    [.Peg],
    ])

print("Generating graph...")

let rootNode = BoardNode(board: board)
let graph = BoardGraph(root: rootNode)
graph.writeToFile("output.xml")

print("Done!")
